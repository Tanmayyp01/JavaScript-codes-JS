// Strings :

let fname = "Mahesh" // String Described.
const hobby ="Swimming"

// Methids to get output :

console.log(fname + hobby) // Old Method
console.log(`My name is ${fname} and my hobby is ${hobby}`);

// Make all characters to Uppercase :
console.log(fname.toUpperCase());

// Make all characters to Lowercase :
console.log(hobby.toLowerCase());

// To get exact length of the string :
console.log(fname.length); // Output - 6
console.log(hobby.length); //Output - 8

// Cutting(trim) String : 
const newString = "    Prince zozo     "
